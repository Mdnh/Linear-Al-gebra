    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        
        x = Integer.parseInt(X.getText());
        y = Integer.parseInt(Y.getText());
        save();
        titles = new String [x]; for(int i = 0; i < x; i++) titles[i] = "x" + (i+1);
        double inverted [][] = new double [y][x];
        for(int i = 0; i < x; i++){
            for(int j = 0; j < y; j++){
                inverted [j][i] = values [i][j];
            }
        }
        values = inverted.clone();
        Objects = new Object [y][x];
        for(int i = 0; i < values.length; i++){
            for(int j = 0; j < values[0].length; j++){
                Objects [i][j] = (Object)values[i][j];
            }
        }
        X.setText(""+y);
        Y.setText(""+x);
        table1.setModel(new javax.swing.table.DefaultTableModel(Objects, titles));
    }//GEN-LAST:event_jButton2ActionPerformed

