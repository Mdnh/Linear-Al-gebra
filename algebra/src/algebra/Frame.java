    void save (){
        if(x != 0 && y != 0){
            values = new double [x][y];
            for(int i = 0; i < x; i++){
                for(int j = 0; j < y; j++){
                    if(table1.getValueAt(i, j) == null)
                    return;
                    values [i][j] = Double.parseDouble(""+table1.getValueAt(i, j));
                }
            }
        }       
    }    
